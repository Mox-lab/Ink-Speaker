-- ============================================================
-- 用户维度小说名唯一约束
-- 同一 owner_id 下 title 不可重复(不同用户允许重名)。
-- 使用 IF NOT EXISTS 保证幂等:Flyway 重跑或全新部署均安全。
-- 注意:若已存在同用户重名数据,本迁移会失败,需先清理重复数据。
-- ============================================================
CREATE UNIQUE INDEX IF NOT EXISTS uq_novel_owner_title ON novel(owner_id, title);
